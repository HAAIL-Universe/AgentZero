def make_confounded_graph(p_confounder=0.5, p_treat_given_conf=None, p_outcome_given_treat_conf=None):
    """Create a simple confounded graph: U -> X, U -> Y, X -> Y.

    U is the confounder, X is treatment, Y is outcome.
    Returns CausalGraph with CPDs.
    """
    g = CausalGraph()
    g.add_node('U', cardinality=2)
    g.add_node('X', cardinality=2)
    g.add_node('Y', cardinality=2)
    g.add_edge('U', 'X')
    g.add_edge('U', 'Y')
    g.add_edge('X', 'Y')

    # P(U)
    u_cpd = Factor(['U'], {'U': 2})
    u_cpd.set_value({'U': 0}, 1 - p_confounder)
    u_cpd.set_value({'U': 1}, p_confounder)
    g.set_cpd('U', u_cpd)

    # P(X | U)
    if p_treat_given_conf is None:
        p_treat_given_conf = {(0,): 0.2, (1,): 0.8}
    x_cpd = Factor(['X', 'U'], {'X': 2, 'U': 2})
    x_cpd.set_value({'X': 0, 'U': 0}, 1 - p_treat_given_conf[(0,)])
    x_cpd.set_value({'X': 1, 'U': 0}, p_treat_given_conf[(0,)])
    x_cpd.set_value({'X': 0, 'U': 1}, 1 - p_treat_given_conf[(1,)])
    x_cpd.set_value({'X': 1, 'U': 1}, p_treat_given_conf[(1,)])
    g.set_cpd('X', x_cpd)

    # P(Y | X, U)
    if p_outcome_given_treat_conf is None:
        p_outcome_given_treat_conf = {
            (0, 0): 0.1, (1, 0): 0.5,  # U=0: treatment helps
            (0, 1): 0.3, (1, 1): 0.9,  # U=1: treatment helps more
        }
    y_cpd = Factor(['Y', 'X', 'U'], {'Y': 2, 'X': 2, 'U': 2})
    for (x, u), p in p_outcome_given_treat_conf.items():
        y_cpd.set_value({'Y': 1, 'X': x, 'U': u}, p)
        y_cpd.set_value({'Y': 0, 'X': x, 'U': u}, 1 - p)
    g.set_cpd('Y', y_cpd)

    return g


def make_mediator_graph():
    """Create a mediated graph: X -> M -> Y with confounder U -> {X, Y}.

    Frontdoor criterion applies through M.
    """
    g = CausalGraph()
    g.add_node('U', cardinality=2)
    g.add_node('X', cardinality=2)
    g.add_node('M', cardinality=2)
    g.add_node('Y', cardinality=2)
    g.add_edge('U', 'X')
    g.add_edge('U', 'Y')
    g.add_edge('X', 'M')
    g.add_edge('M', 'Y')

    # P(U)
    u_cpd = Factor(['U'], {'U': 2})
    u_cpd.set_value({'U': 0}, 0.5)
    u_cpd.set_value({'U': 1}, 0.5)
    g.set_cpd('U', u_cpd)

    # P(X | U)
    x_cpd = Factor(['X', 'U'], {'X': 2, 'U': 2})
    x_cpd.set_value({'X': 0, 'U': 0}, 0.8)
    x_cpd.set_value({'X': 1, 'U': 0}, 0.2)
    x_cpd.set_value({'X': 0, 'U': 1}, 0.3)
    x_cpd.set_value({'X': 1, 'U': 1}, 0.7)
    g.set_cpd('X', x_cpd)

    # P(M | X)
    m_cpd = Factor(['M', 'X'], {'M': 2, 'X': 2})
    m_cpd.set_value({'M': 0, 'X': 0}, 0.9)
    m_cpd.set_value({'M': 1, 'X': 0}, 0.1)
    m_cpd.set_value({'M': 0, 'X': 1}, 0.2)
    m_cpd.set_value({'M': 1, 'X': 1}, 0.8)
    g.set_cpd('M', m_cpd)

    # P(Y | M, U)
    y_cpd = Factor(['Y', 'M', 'U'], {'Y': 2, 'M': 2, 'U': 2})
    y_cpd.set_value({'Y': 1, 'M': 0, 'U': 0}, 0.1)
    y_cpd.set_value({'Y': 0, 'M': 0, 'U': 0}, 0.9)
    y_cpd.set_value({'Y': 1, 'M': 1, 'U': 0}, 0.7)
    y_cpd.set_value({'Y': 0, 'M': 1, 'U': 0}, 0.3)
    y_cpd.set_value({'Y': 1, 'M': 0, 'U': 1}, 0.3)
    y_cpd.set_value({'Y': 0, 'M': 0, 'U': 1}, 0.7)
    y_cpd.set_value({'Y': 1, 'M': 1, 'U': 1}, 0.9)
    y_cpd.set_value({'Y': 0, 'M': 1, 'U': 1}, 0.1)
    g.set_cpd('Y', y_cpd)

    return g


