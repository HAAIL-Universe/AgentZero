# 6. CounterfactualBandit
# ============================================================

class CounterfactualBandit:
    """Uses counterfactual reasoning for regret minimization.

    After each round, reasons: "Would I have gotten a better reward
    if I had chosen a different arm?" Uses twin-network counterfactuals
    from C161 to estimate counterfactual rewards without actually pulling.
    """

    def __init__(self, env, seed=None):
        self.env = env
        self.k = env.k
        self.rng = random.Random(seed)
        self.counts = [0] * self.k
        self.reward_sums = [0.0] * self.k
        self.total = 0

        # Counterfactual reward estimates
        self.cf_counts = [0] * self.k
        self.cf_reward_sums = [0.0] * self.k

        self.engine = CounterfactualEngine(env.graph)

    def select_arm(self, c=2.0):
        """Select arm using combined actual + counterfactual estimates."""
        for a in range(self.k):
            if self.counts[a] + self.cf_counts[a] == 0:
                return a

        ucb = []
        for a in range(self.k):
            total_count = self.counts[a] + self.cf_counts[a]
            total_reward = self.reward_sums[a] + self.cf_reward_sums[a]
            mean = total_reward / max(1, total_count)
            total_pulls = self.total + sum(self.cf_counts)
            bonus = c * math.sqrt(math.log(max(1, total_pulls)) / max(1, total_count))
            ucb.append(mean + bonus)
        return max(range(self.k), key=lambda a: ucb[a])

    def update(self, arm, context, reward):
        """Update with actual reward and counterfactual estimates."""
        self.counts[arm] += 1
        self.reward_sums[arm] += reward
        self.total += 1

        # Compute counterfactual rewards for unchosen arms
        evidence = dict(context)
        evidence[self.env.treatment] = arm
        evidence[self.env.outcome] = reward

        for other_arm in range(self.k):
            if other_arm == arm:
                continue
            try:
                cf_factor = self.engine.query(
                    self.env.outcome,
                    {self.env.treatment: other_arm},
                    evidence
                )
                # Expected counterfactual reward
                card = self.env.graph.cardinalities.get(self.env.outcome, 2)
                cf_reward = 0.0
                for val in range(card):
                    cf_reward += val * cf_factor.get_value({self.env.outcome: val})
                self.cf_counts[other_arm] += 1
                self.cf_reward_sums[other_arm] += cf_reward
            except Exception:
                pass  # Skip if counterfactual query fails

    def run(self, n_rounds, c=2.0):
        arms = []
        rewards = []
        regret = []
        cum_regret = 0.0
        opt = self.env.optimal_reward()

        for _ in range(n_rounds):
            arm = self.select_arm(c)
            context, reward = self.env.pull(arm)
            self.update(arm, context, reward)
            arms.append(arm)
            rewards.append(reward)
            cum_regret += opt - self.env.true_reward(arm)
            regret.append(cum_regret)

        return arms, rewards, regret


