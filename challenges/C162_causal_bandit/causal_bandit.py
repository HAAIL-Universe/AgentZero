class TransferBandit:
    """Transfer causal knowledge across related bandit environments.

    When two environments share causal structure but differ in parameters,
    knowledge from the source can warm-start learning in the target.
    Uses structural similarity to determine transferability.
    """

    def __init__(self, target_env, source_knowledge=None):
        """
        Args:
            target_env: CausalBanditEnv for the target task
            source_knowledge: dict with keys:
                'arm_estimates': list of (mean, count) per arm
                'graph': CausalGraph of the source
                'adjustment_set': set of adjustment variables
        """
        self.env = target_env
        self.k = target_env.k
        self.counts = [0] * self.k
        self.reward_sums = [0.0] * self.k
        self.total = 0
        self.transfer_weight = 0.0

        if source_knowledge:
            self._apply_transfer(source_knowledge)

    def _apply_transfer(self, source):
        """Apply source knowledge with structural similarity weighting."""
        source_graph = source.get('graph')
        target_graph = self.env.graph

        # Compute structural similarity
        if source_graph:
            self.transfer_weight = self._structural_similarity(
                source_graph, target_graph
            )
        else:
            self.transfer_weight = 0.5

        # Transfer arm estimates weighted by similarity
        arm_estimates = source.get('arm_estimates', [])
        for arm in range(min(self.k, len(arm_estimates))):
            mean, count = arm_estimates[arm]
            # Virtual counts from source, discounted by similarity
            virtual_count = count * self.transfer_weight
            self.counts[arm] = int(virtual_count)
            self.reward_sums[arm] = mean * virtual_count
            self.total += int(virtual_count)

    def _structural_similarity(self, g1, g2):
        """Compute structural similarity between two causal graphs."""
        nodes1 = set(g1.nodes)
        nodes2 = set(g2.nodes)
        common = nodes1 & nodes2
        if not common:
            return 0.0

        # Jaccard on nodes
        node_sim = len(common) / len(nodes1 | nodes2)

        # Edge overlap on common nodes
        edges1 = set((p, c) for p, c in g1.edges if p in common and c in common)
        edges2 = set((p, c) for p, c in g2.edges if p in common and c in common)
        if edges1 or edges2:
            edge_sim = len(edges1 & edges2) / len(edges1 | edges2)
        else:
            edge_sim = 1.0 if not edges1 and not edges2 else 0.0

        return 0.5 * node_sim + 0.5 * edge_sim

    def select_arm(self, c=2.0):
        for a in range(self.k):
            if self.counts[a] == 0:
                return a
        ucb = []
        for a in range(self.k):
            mean = self.reward_sums[a] / self.counts[a]
            bonus = c * math.sqrt(math.log(self.total) / self.counts[a])
            ucb.append(mean + bonus)
        return max(range(self.k), key=lambda a: ucb[a])

    def update(self, arm, reward):
        self.counts[arm] += 1
        self.reward_sums[arm] += reward
        self.total += 1

    def run(self, n_rounds, c=2.0):
        arms = []
        rewards = []
        regret = []
        cum_regret = 0.0
        opt = self.env.optimal_reward()

        for _ in range(n_rounds):
            arm = self.select_arm(c)
            _, reward = self.env.pull(arm)
            self.update(arm, reward)
            arms.append(arm)
            rewards.append(reward)
            cum_regret += opt - self.env.true_reward(arm)
            regret.append(cum_regret)

        return arms, rewards, regret

    def export_knowledge(self):
        """Export learned knowledge for transfer to another bandit."""
        estimates = []
        for a in range(self.k):
            mean = self.reward_sums[a] / max(1, self.counts[a])
            estimates.append((mean, self.counts[a]))
        return {
            'arm_estimates': estimates,
            'graph': self.env.graph,
            'adjustment_set': BackdoorCriterion.find_adjustment_set(
                self.env.graph, self.env.treatment, self.env.outcome
            )
        }


