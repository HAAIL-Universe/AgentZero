# 9. CausalBanditAnalyzer
# ============================================================

class CausalBanditAnalyzer:
    """Analyze and compare causal bandit algorithms."""

    @staticmethod
    def compute_regret(arms, env):
        """Compute cumulative regret for a sequence of arm pulls."""
        opt = env.optimal_reward()
        regret = []
        cum = 0.0
        for arm in arms:
            cum += opt - env.true_reward(arm)
            regret.append(cum)
        return regret

    @staticmethod
    def arm_distribution(arms, k):
        """Compute fraction of times each arm was pulled."""
        counts = [0] * k
        for a in arms:
            if 0 <= a < k:
                counts[a] += 1
        total = sum(counts)
        if total == 0:
            return [0.0] * k
        return [c / total for c in counts]

    @staticmethod
    def compare_algorithms(env, algorithms, n_rounds, n_trials=5):
        """Compare multiple algorithms over multiple trials.

        Args:
            env: CausalBanditEnv (will be reseeded each trial)
            algorithms: dict of name -> factory function (takes env, returns bandit)
            n_rounds: rounds per trial
            n_trials: number of independent trials

        Returns:
            dict of name -> {
                'mean_regret': final mean cumulative regret,
                'std_regret': std of final cumulative regret,
                'mean_arm_dist': average arm distribution,
                'optimal_arm_frac': fraction of times optimal arm chosen
            }
        """
        results = {}
        opt_arm = env.optimal_arm()

        for name, factory in algorithms.items():
            final_regrets = []
            arm_dists = []
            opt_fracs = []

            for trial in range(n_trials):
                env.rng = random.Random(trial * 1000)
                bandit = factory(env)

                if hasattr(bandit, 'run'):
                    arms, rewards, regret = bandit.run(n_rounds)
                else:
                    arms = []
                    rewards = []
                    regret = []
                    cum_r = 0.0
                    for _ in range(n_rounds):
                        arm = bandit.select_arm()
                        _, reward = env.pull(arm)
                        bandit.update(arm, reward)
                        arms.append(arm)
                        rewards.append(reward)
                        cum_r += env.optimal_reward() - env.true_reward(arm)
                        regret.append(cum_r)

                final_regrets.append(regret[-1] if regret else 0)
                arm_dists.append(CausalBanditAnalyzer.arm_distribution(arms, env.k))
                opt_count = sum(1 for a in arms if a == opt_arm)
                opt_fracs.append(opt_count / len(arms) if arms else 0)

            mean_regret = sum(final_regrets) / len(final_regrets)
            mean_sq = sum(r**2 for r in final_regrets) / len(final_regrets)
            std_regret = math.sqrt(max(0, mean_sq - mean_regret**2))

            mean_dist = [0.0] * env.k
            for dist in arm_dists:
                for a in range(env.k):
                    mean_dist[a] += dist[a]
            mean_dist = [d / n_trials for d in mean_dist]

            results[name] = {
                'mean_regret': mean_regret,
                'std_regret': std_regret,
                'mean_arm_dist': mean_dist,
                'optimal_arm_frac': sum(opt_fracs) / len(opt_fracs),
            }

        return results

    @staticmethod
    def causal_advantage(env, n_rounds=200, n_trials=5):
        """Measure the advantage of causal methods over standard UCB.

        Returns ratio of standard UCB regret to causal UCB regret.
        Ratio > 1 means causal method is better.
        """
        std_regrets = []
        causal_regrets = []

        for trial in range(n_trials):
            # Standard UCB (no causal adjustment)
            env.rng = random.Random(trial * 100)
            std_ucb = CausalUCB(env, c=2.0)
            std_ucb.use_causal = False  # Disable causal adjustment
            _, _, std_reg = std_ucb.run(n_rounds)
            std_regrets.append(std_reg[-1] if std_reg else 0)

            # Causal UCB
            env.rng = random.Random(trial * 100)
            causal_ucb = CausalUCB(env, c=2.0)
            _, _, causal_reg = causal_ucb.run(n_rounds)
            causal_regrets.append(causal_reg[-1] if causal_reg else 0)

        mean_std = sum(std_regrets) / max(1, len(std_regrets))
        mean_causal = sum(causal_regrets) / max(1, len(causal_regrets))

        return {
            'standard_regret': mean_std,
            'causal_regret': mean_causal,
            'advantage_ratio': mean_std / mean_causal if mean_causal > 0 else float('inf'),
        }


