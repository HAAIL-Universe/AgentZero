# 8. BudgetedCausalBandit
# ============================================================

class BudgetedCausalBandit:
    """Bandit where interventions have costs. Optimize net reward (reward - cost).

    Different intervention targets have different costs. Observing (no intervention)
    is free but yields confounded data. The agent balances intervention cost
    against information gain.
    """

    def __init__(self, env, intervention_costs=None, observation_budget=0.5):
        """
        Args:
            env: CausalBanditEnv
            intervention_costs: dict mapping arm -> cost (default: uniform cost 0.1)
            observation_budget: fraction of rounds to allocate to free observations
        """
        self.env = env
        self.k = env.k
        self.observation_budget = observation_budget

        if intervention_costs is None:
            self.costs = {a: 0.1 for a in range(self.k)}
        else:
            self.costs = dict(intervention_costs)

        self.counts = [0] * self.k
        self.reward_sums = [0.0] * self.k
        self.cost_spent = 0.0
        self.total = 0

        # Observational data for free learning
        self.obs_counts = defaultdict(int)
        self.obs_reward_sums = defaultdict(float)

    def select_arm(self, budget_remaining, c=2.0):
        """Select arm considering cost-effectiveness."""
        # Decide whether to observe (free) or intervene (costly)
        if self.total > 0 and self.rng.random() < self.observation_budget:
            return -1  # Signal to observe instead

        for a in range(self.k):
            if self.counts[a] == 0:
                return a

        # Cost-adjusted UCB: (reward - cost) / cost for efficiency
        ucb = []
        for a in range(self.k):
            mean = self.reward_sums[a] / self.counts[a]
            net = mean - self.costs.get(a, 0)
            bonus = c * math.sqrt(math.log(self.total) / self.counts[a])
            ucb.append(net + bonus)
        return max(range(self.k), key=lambda a: ucb[a])

    def update_intervention(self, arm, reward):
        self.counts[arm] += 1
        self.reward_sums[arm] += reward
        self.cost_spent += self.costs.get(arm, 0)
        self.total += 1

    def update_observation(self, obs_data):
        """Learn from free observational data."""
        treatment_val = obs_data.get(self.env.treatment)
        outcome_val = obs_data.get(self.env.outcome)
        if treatment_val is not None and outcome_val is not None:
            self.obs_counts[treatment_val] += 1
            self.obs_reward_sums[treatment_val] += outcome_val

    def run(self, n_rounds, budget=None, c=2.0):
        """Run with optional total budget constraint."""
        self.rng = random.Random(42)
        arms = []
        rewards = []
        net_rewards = []
        cum_net = 0.0

        for _ in range(n_rounds):
            if budget is not None and self.cost_spent >= budget:
                # Budget exhausted -- observe only
                obs = self.env.observe()
                self.update_observation(obs)
                arms.append(-1)
                rewards.append(0)
                net_rewards.append(cum_net)
                continue

            arm = self.select_arm(budget, c)
            if arm == -1:
                obs = self.env.observe()
                self.update_observation(obs)
                arms.append(-1)
                rewards.append(0)
                net_rewards.append(cum_net)
            else:
                _, reward = self.env.pull(arm)
                self.update_intervention(arm, reward)
                net = reward - self.costs.get(arm, 0)
                cum_net += net
                arms.append(arm)
                rewards.append(reward)
                net_rewards.append(cum_net)

        return arms, rewards, net_rewards

    def best_cost_effective_arm(self):
        """Return arm with best observed net reward."""
        best = -1
        best_net = float('-inf')
        for a in range(self.k):
            if self.counts[a] > 0:
                mean = self.reward_sums[a] / self.counts[a]
                net = mean - self.costs.get(a, 0)
                if net > best_net:
                    best_net = net
                    best = a
        return best


