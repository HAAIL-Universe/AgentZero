class InterventionalBandit:
    """Bandit that can intervene on intermediate causal variables, not just treatment.

    In a causal graph X -> M -> Y with confounder U -> {X, Y},
    intervening on M (the mediator) can bypass confounding.
    This bandit discovers which intervention targets yield best rewards.
    """

    def __init__(self, graph, outcome, interventional_vars=None, seed=None):
        """
        Args:
            graph: CausalGraph
            outcome: reward variable
            interventional_vars: list of variables we can intervene on
                                 (None = all non-outcome nodes)
        """
        self.graph = graph
        self.outcome = outcome
        self.rng = random.Random(seed)

        if interventional_vars is None:
            self.interventional_vars = [n for n in graph.nodes if n != outcome]
        else:
            self.interventional_vars = list(interventional_vars)

        # Build action space: all (variable, value) intervention pairs
        self.actions = []
        for var in self.interventional_vars:
            card = graph.cardinalities.get(var, 2)
            for val in range(card):
                self.actions.append((var, val))

        self.k = len(self.actions)
        self.counts = [0] * self.k
        self.reward_sums = [0.0] * self.k
        self.total = 0

        # Precompute true rewards for each intervention
        self._topo_order = graph.topological_sort()
        self._true_rewards = []
        for var, val in self.actions:
            factor = graph.interventional_query(outcome, {var: val})
            card = graph.cardinalities.get(outcome, 2)
            ev = sum(v * factor.get_value({outcome: v}) for v in range(card))
            self._true_rewards.append(ev)

    def optimal_action(self):
        """Return index of optimal intervention."""
        return max(range(self.k), key=lambda i: self._true_rewards[i])

    def optimal_reward(self):
        return self._true_rewards[self.optimal_action()]

    def select_action(self, c=2.0):
        """UCB-based action selection over interventions."""
        for i in range(self.k):
            if self.counts[i] == 0:
                return i
        ucb = []
        for i in range(self.k):
            mean = self.reward_sums[i] / self.counts[i]
            bonus = c * math.sqrt(math.log(self.total) / self.counts[i])
            ucb.append(mean + bonus)
        return max(range(self.k), key=lambda i: ucb[i])

    def pull(self, action_idx):
        """Execute intervention and observe reward."""
        var, val = self.actions[action_idx]
        mutilated = self.graph.mutilate({var: val})
        env = CausalBanditEnv(mutilated, var, self.outcome, seed=self.rng.randint(0, 10**6))
        # Sample from the mutilated graph
        sample = env._sample_from_graph(mutilated, {var: val})
        return sample[self.outcome]

    def update(self, action_idx, reward):
        self.counts[action_idx] += 1
        self.reward_sums[action_idx] += reward
        self.total += 1

    def run(self, n_rounds, c=2.0):
        """Run for n_rounds."""
        actions = []
        rewards = []
        regret = []
        cum_regret = 0.0
        opt = self.optimal_reward()

        for _ in range(n_rounds):
            idx = self.select_action(c)
            reward = self.pull(idx)
            self.update(idx, reward)
            actions.append(idx)
            rewards.append(reward)
            cum_regret += opt - self._true_rewards[idx]
            regret.append(cum_regret)

        return actions, rewards, regret


