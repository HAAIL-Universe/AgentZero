class CausalBanditEnv:
    """Bandit environment where rewards are generated by a causal DAG.

    Arms correspond to interventions on a treatment variable.
    Confounders, mediators, and other causal structure affect rewards.
    The agent can observe some variables but must reason about hidden ones.
    """

    def __init__(self, graph, treatment, outcome, observable=None, seed=None):
        """
        Args:
            graph: CausalGraph with CPDs defining the data-generating process
            treatment: name of the treatment variable (arms = its values)
            outcome: name of the outcome/reward variable
            observable: set of variables the agent can observe (None = all non-treatment/outcome)
            seed: random seed
        """
        self.graph = graph
        self.treatment = treatment
        self.outcome = outcome
        self.k = graph.cardinalities.get(treatment, 2)  # number of arms
        self.rng = random.Random(seed)

        if observable is None:
            self.observable = set(graph.nodes) - {treatment, outcome}
        else:
            self.observable = set(observable)

        # Precompute topological order for sampling
        self._topo_order = graph.topological_sort()
        self._true_arm_rewards = self._compute_true_rewards()

    def _compute_true_rewards(self):
        """Compute E[Y | do(X=a)] for each arm a."""
        rewards = []
        for a in range(self.k):
            factor = self.graph.interventional_query(
                self.outcome, {self.treatment: a}
            )
            # Expected value: sum over outcome values weighted by probability
            ev = 0.0
            card = self.graph.cardinalities.get(self.outcome, 2)
            for val in range(card):
                ev += val * factor.get_value({self.outcome: val})
            rewards.append(ev)
        return rewards

    def optimal_arm(self):
        """Return the arm with highest true expected reward under do()."""
        return max(range(self.k), key=lambda a: self._true_arm_rewards[a])

    def optimal_reward(self):
        """Return the expected reward of the optimal arm."""
        return self._true_arm_rewards[self.optimal_arm()]

    def pull(self, arm):
        """Pull an arm (intervene on treatment=arm), return (context, reward).

        context: dict of observable variable values (sampled from the SCM)
        reward: sampled outcome value
        """
        # Sample from mutilated graph (do(treatment=arm))
        mutilated = self.graph.mutilate({self.treatment: arm})
        sample = self._sample_from_graph(mutilated, {self.treatment: arm})
        context = {v: sample[v] for v in self.observable if v in sample}
        reward = sample[self.outcome]
        return context, reward

    def observe(self):
        """Observe a data point from the observational distribution (no intervention).

        Returns: dict of all variable values sampled from the original graph.
        """
        return self._sample_from_graph(self.graph, {})

    def _sample_from_graph(self, graph, fixed):
        """Sample all variables from graph given fixed values."""
        sample = dict(fixed)
        for node in self._topo_order:
            if node in sample:
                continue
            parents = graph.get_parents(node)
            if node in graph.cpds:
                cpd = graph.cpds[node]
                # Build parent assignment
                parent_assignment = {p: sample[p] for p in parents if p in sample}
                # Get conditional distribution
                card = graph.cardinalities.get(node, 2)
                probs = []
                for val in range(card):
                    assignment = dict(parent_assignment)
                    assignment[node] = val
                    probs.append(cpd.get_value(assignment))
                # Normalize
                total = sum(probs)
                if total > 0:
                    probs = [p / total for p in probs]
                else:
                    probs = [1.0 / card] * card
                # Sample
                r = self.rng.random()
                cumulative = 0.0
                sampled_val = card - 1
                for val, p in enumerate(probs):
                    cumulative += p
                    if r < cumulative:
                        sampled_val = val
                        break
                sample[node] = sampled_val
            else:
                # No CPD -- sample uniformly
                card = graph.cardinalities.get(node, 2)
                sample[node] = self.rng.randint(0, card - 1)
        return sample

    def true_reward(self, arm):
        """Return E[Y | do(X=arm)]."""
        return self._true_arm_rewards[arm]


