# 7. CausalLinUCB
# ============================================================

class CausalLinUCB:
    """Linear contextual bandit with causal feature selection.

    Uses the causal graph to select only causally relevant features
    (parents of the outcome, not descendants or colliders), avoiding
    the spurious correlations that confound standard contextual bandits.
    """

    def __init__(self, env, alpha=1.0):
        self.env = env
        self.k = env.k
        self.alpha = alpha

        # Select causal features: parents of outcome that aren't descendants of treatment
        self.causal_features = self._select_causal_features()
        self.d = max(1, len(self.causal_features))  # feature dimension

        # LinUCB parameters per arm
        self.A = {}  # d x d identity matrices
        self.b = {}  # d x 1 zero vectors
        for a in range(self.k):
            self.A[a] = [[1.0 if i == j else 0.0 for j in range(self.d)]
                         for i in range(self.d)]
            self.b[a] = [0.0] * self.d

    def _select_causal_features(self):
        """Select causally relevant features using graph structure."""
        graph = self.env.graph
        outcome = self.env.outcome
        treatment = self.env.treatment

        # Get parents of outcome (direct causes)
        parents = set(graph.get_parents(outcome))

        # Get ancestors of outcome (potential confounders)
        ancestors = graph.ancestors(outcome)

        # Exclude treatment and outcome themselves
        candidates = (parents | ancestors) - {treatment, outcome}

        # Exclude descendants of treatment (mediators can induce bias if conditioned on)
        descendants_of_treatment = graph.descendants(treatment)

        # Safe features: ancestors of outcome that aren't descendants of treatment
        safe = candidates - descendants_of_treatment

        # If nothing safe, use observable parents
        if not safe:
            safe = parents - {treatment, outcome}

        # Intersect with observable variables
        features = sorted(safe & self.env.observable)
        return features if features else sorted(self.env.observable)[:1]

    def _get_feature_vector(self, context):
        """Extract causal feature vector from context."""
        return [float(context.get(f, 0)) for f in self.causal_features]

    def select_arm(self, context):
        """Select arm using LinUCB with causal features."""
        x = self._get_feature_vector(context)
        ucb_values = []

        for a in range(self.k):
            # Compute A_inv * x
            A_inv = self._matrix_inverse(self.A[a])
            theta = self._mat_vec(A_inv, self.b[a])
            pred = sum(theta[i] * x[i] for i in range(self.d))

            # Confidence bonus: sqrt(x^T A_inv x)
            Ax = self._mat_vec(A_inv, x)
            var = sum(x[i] * Ax[i] for i in range(self.d))
            bonus = self.alpha * math.sqrt(max(0, var))

            ucb_values.append(pred + bonus)

        return max(range(self.k), key=lambda a: ucb_values[a])

    def update(self, arm, context, reward):
        """Update LinUCB parameters."""
        x = self._get_feature_vector(context)

        # A += x * x^T
        for i in range(self.d):
            for j in range(self.d):
                self.A[arm][i][j] += x[i] * x[j]

        # b += reward * x
        for i in range(self.d):
            self.b[arm][i] += reward * x[i]

    def _matrix_inverse(self, M):
        """Simple matrix inverse for small matrices."""
        n = len(M)
        if n == 1:
            return [[1.0 / M[0][0]]] if M[0][0] != 0 else [[1e10]]

        # Augmented matrix [M | I]
        aug = [row[:] + [1.0 if i == j else 0.0 for j in range(n)]
               for i, row in enumerate(M)]

        for col in range(n):
            # Pivot
            max_row = max(range(col, n), key=lambda r: abs(aug[r][col]))
            aug[col], aug[max_row] = aug[max_row], aug[col]

            pivot = aug[col][col]
            if abs(pivot) < 1e-12:
                aug[col][col] = 1e-12
                pivot = 1e-12

            for j in range(2 * n):
                aug[col][j] /= pivot

            for row in range(n):
                if row != col:
                    factor = aug[row][col]
                    for j in range(2 * n):
                        aug[row][j] -= factor * aug[col][j]

        return [row[n:] for row in aug]

    def _mat_vec(self, M, v):
        """Matrix-vector product."""
        return [sum(M[i][j] * v[j] for j in range(len(v))) for i in range(len(M))]

    def run(self, n_rounds):
        arms = []
        rewards = []
        regret = []
        cum_regret = 0.0
        opt = self.env.optimal_reward()

        for _ in range(n_rounds):
            # Get context from an observation
            context, _ = self.env.pull(0)  # Pull arm 0 just for context
            arm = self.select_arm(context)
            _, reward = self.env.pull(arm)
            self.update(arm, context, reward)
            arms.append(arm)
            rewards.append(reward)
            cum_regret += opt - self.env.true_reward(arm)
            regret.append(cum_regret)

        return arms, rewards, regret


