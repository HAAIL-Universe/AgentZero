# 3. CausalThompsonSampling
# ============================================================

class CausalThompsonSampling:
    """Thompson sampling with causal priors.

    Uses causal graph structure to set informative priors on arm rewards.
    When an arm's effect is identifiable via do-calculus, uses the causal
    effect estimate to initialize the prior, accelerating convergence.
    """

    def __init__(self, env, prior_strength=1.0):
        self.env = env
        self.k = env.k
        self.rng = random.Random(42)
        self.prior_strength = prior_strength

        # Beta distribution parameters (for binary rewards)
        self.alphas = [1.0] * self.k
        self.betas = [1.0] * self.k

        # Try to set causal priors
        self._set_causal_priors()

    def _set_causal_priors(self):
        """Use causal effect estimates to set informative priors."""
        try:
            adjustment_set = BackdoorCriterion.find_adjustment_set(
                self.env.graph, self.env.treatment, self.env.outcome
            )
            if adjustment_set is not None:
                for arm in range(self.k):
                    factor = self.env.graph.interventional_query(
                        self.env.outcome, {self.env.treatment: arm}
                    )
                    # Get P(Y=1 | do(X=arm))
                    p = factor.get_value({self.env.outcome: 1})
                    # Set prior proportional to causal estimate
                    n = self.prior_strength * 10
                    self.alphas[arm] = 1.0 + p * n
                    self.betas[arm] = 1.0 + (1 - p) * n
        except Exception:
            pass  # Fall back to uniform priors

    def select_arm(self):
        """Sample from posterior and pick highest."""
        samples = []
        for a in range(self.k):
            # Beta distribution sampling via inverse transform
            sample = self._beta_sample(self.alphas[a], self.betas[a])
            samples.append(sample)
        return max(range(self.k), key=lambda a: samples[a])

    def _beta_sample(self, alpha, beta_param):
        """Sample from Beta(alpha, beta) using gamma variates."""
        # Use Python's random.gammavariate
        x = self.rng.gammavariate(alpha, 1.0)
        y = self.rng.gammavariate(beta_param, 1.0)
        return x / (x + y) if (x + y) > 0 else 0.5

    def update(self, arm, reward):
        """Update posterior after observing reward."""
        if reward > 0:
            self.alphas[arm] += 1.0
        else:
            self.betas[arm] += 1.0

    def run(self, n_rounds):
        """Run for n_rounds."""
        arms = []
        rewards = []
        regret = []
        cum_regret = 0.0
        opt_reward = self.env.optimal_reward()

        for _ in range(n_rounds):
            arm = self.select_arm()
            _, reward = self.env.pull(arm)
            self.update(arm, reward)
            arms.append(arm)
            rewards.append(reward)
            cum_regret += opt_reward - self.env.true_reward(arm)
            regret.append(cum_regret)

        return arms, rewards, regret


