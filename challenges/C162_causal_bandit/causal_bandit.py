# ============================================================

class CausalUCB:
    """UCB bandit that uses causal bounds for tighter confidence intervals.

    When confounders exist, the observational mean != interventional mean.
    CausalUCB uses the backdoor/frontdoor criterion to adjust estimates,
    giving tighter bounds than standard UCB which ignores confounding.
    """

    def __init__(self, env, c=2.0):
        self.env = env
        self.k = env.k
        self.c = c
        self.counts = [0] * self.k
        self.reward_sums = [0.0] * self.k
        self.total = 0

        # Check if backdoor adjustment is possible
        self.adjustment_set = BackdoorCriterion.find_adjustment_set(
            env.graph, env.treatment, env.outcome
        )
        self.use_causal = self.adjustment_set is not None

        # Track stratified counts for adjustment
        if self.use_causal:
            # For each arm, track reward by adjustment set values
            self._strat_counts = defaultdict(lambda: defaultdict(int))
            self._strat_reward_sums = defaultdict(lambda: defaultdict(float))

    def select_arm(self):
        """Select arm using causal UCB criterion."""
        # Explore unvisited arms first
        for a in range(self.k):
            if self.counts[a] == 0:
                return a

        ucb_values = []
        for a in range(self.k):
            if self.use_causal:
                mean = self._adjusted_mean(a)
            else:
                mean = self.reward_sums[a] / self.counts[a]
            bonus = self.c * math.sqrt(math.log(self.total) / self.counts[a])
            ucb_values.append(mean + bonus)
        return max(range(self.k), key=lambda a: ucb_values[a])

    def _adjusted_mean(self, arm):
        """Compute backdoor-adjusted mean reward for an arm."""
        if self.counts[arm] == 0:
            return 0.0

        # Weighted average over adjustment set strata
        strat_counts = self._strat_counts[arm]
        strat_rewards = self._strat_reward_sums[arm]
        total_strat = sum(strat_counts.values())
        if total_strat == 0:
            return self.reward_sums[arm] / max(1, self.counts[arm])

        # Weight by marginal distribution of adjustment variables
        weighted_sum = 0.0
        total_weight = 0.0
        for key, count in strat_counts.items():
            if count > 0:
                mean_in_stratum = strat_rewards[key] / count
                weight = count  # Use empirical frequency as weight
                weighted_sum += mean_in_stratum * weight
                total_weight += weight

        return weighted_sum / total_weight if total_weight > 0 else 0.0

    def update(self, arm, context, reward):
        """Update estimates after pulling arm."""
        self.counts[arm] += 1
        self.reward_sums[arm] += reward
        self.total += 1

        if self.use_causal and context:
            # Stratify by adjustment set values
            key = tuple(sorted(
                (v, context[v]) for v in self.adjustment_set if v in context
            ))
            self._strat_counts[arm][key] += 1
            self._strat_reward_sums[arm][key] += reward

    def run(self, n_rounds):
        """Run for n_rounds, return (arms_chosen, rewards, cumulative_regret)."""
        arms = []
        rewards = []
        regret = []
        cum_regret = 0.0
        opt_reward = self.env.optimal_reward()

        for _ in range(n_rounds):
            arm = self.select_arm()
            context, reward = self.env.pull(arm)
            self.update(arm, context, reward)
            arms.append(arm)
            rewards.append(reward)
            cum_regret += opt_reward - self.env.true_reward(arm)
            regret.append(cum_regret)

        return arms, rewards, regret


